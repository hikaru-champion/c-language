// sum()
int
sum(int a, int b)
{
	int return_value;

	return_value = a + b;

	return return_value;
}


// sub()
int
sub(int a, int b)
{
	int return_value;

	return_value = a - b;

	return return_value;
}


// mul()
int
mul(int a, int b)
{
	int return_value;

	return_value = a * b;

	return return_value;
}


// div()
int
div(int a, int b)
{
	int return_value;

	return_value = a / b;

	return return_value;
}
